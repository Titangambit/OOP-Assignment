public class Arithmetic {
    public static void main(String[] args) {
        int a = 100;
        int b = 5;
        int add = a + b;
        int subtract = a - b;
        int multiply = a * b;
        int divide = a / b;
        int remainder = b % a;
        System.out.println(add);
        System.out.println(subtract);
        System.out.println(multiply);
        System.out.println(divide);
        System.out.println(remainder);
    }
}