public class Relational{
    public static void main(String[] args){
        int a = 200;
        int b = 150;
        System.out.println(a + " is greater than " + b);
        System.out.println(a>b);
        System.out.println(a<b);
        System.out.println(a==b);
        System.out.println(a!=b);
    }
}